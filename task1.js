// Take four parameters. Multiply the four numbers and then return the result

// Function for multiply four numbers
function fourMultiply(num1, num2, num3, num4) {
	return num1 * num2 * num3 * num4;
}

// Calling the function for multiply four numbers
let result = fourMultiply(10, 3, 7, 2);
console.log(result);
